# format() মেথডের কাজ: স্ট্রিং-এর মধ্যে প্লেসহোল্ডার { } আছে
# এই প্লেসহোল্ডারগুলোকে format() এ দেয়া ভ্যালু দিয়ে রিপ্লেস করা হয়

# উদাহরণ:
print("number from {0} to {1} to {2} are: ".format(1, 5, 10))

# এখানে {0}, {1}, {2} হলো placeholder
# format(1, 5, 10) মানে হলো:
#   {0} -> 1 বসবে
#   {1} -> 5 বসবে
#   {2} -> 10 বসবে

# তাই আউটপুট হবে:
# number from 1 to 5 to 10 are: