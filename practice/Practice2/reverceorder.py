# ইউজারের কাছ থেকে একটি সংখ্যা (n) ইনপুট নিচ্ছে
# int() দিয়ে স্ট্রিং ইনপুটকে পূর্ণসংখ্যায় (integer) কনভার্ট করা হচ্ছে 
n = int(input("Enter the value of n: "))

# এখানে আমরা একটি স্ট্রিং প্রিন্ট করছি যেখানে format() মেথড ব্যবহার হয়েছে
# "numbers from {0} to {1} are:" -> এর মধ্যে {0} আর {1} হলো placeholder
# format(n, 1) মানে {0} জায়গায় n বসবে আর {1} জায়গায় 1 বসবে
# যেমন যদি n = 5 হয় তবে আউটপুট হবে:
# numbers from 5 to 1 are:
print("numbers from {0} to {1} are: ".format(n,1))

# এখন আমরা একটি for লুপ ব্যবহার করছি
# range(n, 0, -1) মানে হলো n থেকে শুরু করে 1 পর্যন্ত নামতে নামতে সংখ্যা জেনারেট করবে
# এখানে step = -1, তাই এক এক করে সংখ্যা কমবে
# যেমন n = 5 হলে সংখ্যা আসবে: 5, 4, 3, 2, 1
for i in range(n, 0, -1):
    # প্রতিবার লুপে i এর মান প্রিন্ট হবে
    print(i)

# এই কোডের কাজ: ইউজার যে সংখ্যা দেবে, সেই সংখ্যা থেকে শুরু করে 1 পর্যন্ত সব সংখ্যা উল্টো ক্রমে দেখানো

